// ─── Enrichment status helper ────────────────────────────────────────────────
// Returns true if a product has been manually enriched with full data.
// Used by the PDP to know whether to show rich UI or basic fallback.
export function isEnriched(product) {
  return !!(product?.images?.length > 0 || product?.features?.length > 0);
}

// ─── Variant color context for AI matching ───────────────────────────────────
// Returns a flat list of color/material keywords from a product's variants.
// Passed to productMatcher so it can weight results toward selected variant colors.
export function getVariantColorContext(product) {
  if (!product?.variants?.length) return [];
  return product.variants.flatMap(v => {
    const words = (v.label || '').toLowerCase().split(/[\s/,]+/);
    return words.filter(w => w.length > 2);
  });
}

export function getProductById(id) {
  return PRODUCT_CATALOG.find((p) => p.id === id) || null;
}

export function getProductsByIds(ids = []) {
  return ids.map((id) => getProductById(id)).filter(Boolean);
}

export function getProductsByCategory(category) {
  return PRODUCT_CATALOG.filter((p) => p.category === category);
}

export function getProductsByRoomType(roomType) {
  return PRODUCT_CATALOG.filter((p) =>
    Array.isArray(p.roomType) ? p.roomType.includes(roomType) : p.roomType === roomType
  );
}

export function getProductsByStyle(style) {
  return PRODUCT_CATALOG.filter((p) => (p.styles || []).includes(style));
}
