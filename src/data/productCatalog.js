// ─── Lookup helpers ───────────────────────────────────────────────────────────

export function getProductById(id) {
  return PRODUCT_CATALOG.find((p) => p.id === id) || null;
}

export function getProductsByIds(ids = []) {
  return ids.map((id) => getProductById(id)).filter(Boolean);
}

export function getProductsByCategory(category) {
  return PRODUCT_CATALOG.filter((p) => p.category === category);
}

export function getProductsByRoomType(roomType) {
  return PRODUCT_CATALOG.filter((p) => p.roomType.includes(roomType));
}

export function getProductsByStyle(style) {
  return PRODUCT_CATALOG.filter((p) => p.styles.includes(style));
}
